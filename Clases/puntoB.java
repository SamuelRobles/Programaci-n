package Clases;

public class puntoB {
	private double x;
	private double y;
	
	puntoB () {
		x=0;
		y=0;
	}

	puntoB (double x2,double y2) {
		x=x2;
		y=y2;
	}
	
	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	
}
